package dezprimeirosnumeros;

/**
 * Este programa fai que se mostren por pantalla os 10 primeiros numeros 
 * mediante un bucle
 * @author ivan
 */
public class DezPrimeirosNumeros {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        // facemos que mostre por pantalla cada numero ata 10 cun bucle for
        for (int i=1; i<=10; i++) {
            System.out.println(+i);
        }
    }
    
}
