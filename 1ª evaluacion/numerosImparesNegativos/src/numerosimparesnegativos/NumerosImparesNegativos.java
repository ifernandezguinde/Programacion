package numerosimparesnegativos;

/**
 * mostra os 10 primeiros numeros negativos impares
 * @author ivan
 */
public class NumerosImparesNegativos {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
         // mandamoslle mostrar o numero por pantalla restandolle 2 en cada bucle
        for (int i =-1; i>-20; i-=2) {
            System.out.println(+i);
        }
    }
    
}
