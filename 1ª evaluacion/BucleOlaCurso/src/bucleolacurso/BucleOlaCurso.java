package bucleolacurso;

/**
 * fai un bucle onde mostra por pantalla "Ola Curso" sete veces
 * @author ivan
 */
public class BucleOlaCurso {
    

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        // facemos que repita Ola Curso sete veces mediante un bucle con for
        for (int i = 0; i < 7; i++) {
            System.out.println("Ola Curso");
        }
        
    }
    
}
